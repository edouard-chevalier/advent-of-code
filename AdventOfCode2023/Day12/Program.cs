void Compute( int lineNo){
    var line = lines[lineNo];
    var parts = line.Split(" ");
    var seq = parts[1].Split(",", StringSplitOptions.RemoveEmptyEntries).Select(int.Parse).ToArray();

    seq = Enumerable.Repeat(seq,nbRepeat).SelectMany(c=>c).ToArray();
    var val = parts[0].Trim();
    string newVal = string.Join("?",Enumerable.Repeat(val,nbRepeat));
    val = newVal;

    var cache = new Dictionary<(int,int),long>();
    long Count( int start,int nextBroken) {
        if( start >= val.Length && nextBroken == seq.Length) {
            return 1;
        }
        if( start >= val.Length) {
            return 0;
        }
        /*if( nextBroken == seq.Length) {
            return 0;
        }*/

        if( cache.TryGetValue((start,nextBroken), out var count)) {
            return count;
        }

        if (val[start] == '.' || val[start] == '?') {
            count+=Count(start+1,nextBroken);
        }
        if ((val[start] == '#' || val[start] == '?') && nextBroken < seq.Length) {
            int left = val.Length - start;
            if (left == seq[nextBroken] || (left > seq[nextBroken] && val[start+seq[nextBroken]] != '#')) {
                bool allPossiblyBroken = true;
                for (int i = 0; i < seq[nextBroken]; i++) {
                    if (val[start + i] == '.') {
                        allPossiblyBroken = false;
                        break;
                    }
                }

                if (allPossiblyBroken) {
                    count+=Count(start+seq[nextBroken] +1,nextBroken+1);
                }
            }
            
        }
        
        cache[(start,nextBroken)] = count;
        return count;
    }

    var count = Count(0, 0);
    Interlocked.Increment(ref progress);
    Console.WriteLine($"{lineNo}: {count}. {progress}/{lines.Length}");
    res[lineNo]=count;
}
